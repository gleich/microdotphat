pub fn lookup_glyph(ch: char) -> Option<[u8; 5]> {
    let cp = ch as u32;
    if cp > 0xFFFF {
        return None;
    }
    let key = cp as u16;
    FONT.binary_search_by_key(&key, |&(k, _)| k)
        .ok()
        .map(|idx| FONT[idx].1) // [u8;5] is Copy, so returning by value is fine
}

